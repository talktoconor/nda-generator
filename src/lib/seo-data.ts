// ── Lookup helpers ──

const typeSet = new Set(NDA_TYPES.map((t) => t.slug));
const industrySet = new Set(INDUSTRIES.map((i) => i.slug));
const useCaseSet = new Set(USE_CASES.map((u) => u.slug));
const stateSet = new Set(US_STATES_SEO.map((s) => s.slug));

export type PageCategory = "type" | "industry" | "use-case" | "state" | "cross" | null;

export function classifySlug(segments: string[]): {
  category: PageCategory;
  data: Record<string, string>;
} {
  if (segments.length === 1) {
    const s = segments[0];
    if (typeSet.has(s)) {
      const t = NDA_TYPES.find((x) => x.slug === s)!;
      return { category: "type", data: { slug: s, title: t.title, short: t.short } };
    }
    if (industrySet.has(s)) {
      const i = INDUSTRIES.find((x) => x.slug === s)!;
      return { category: "industry", data: { slug: s, title: i.title } };
    }
    if (useCaseSet.has(s)) {
      const u = USE_CASES.find((x) => x.slug === s)!;
      return { category: "use-case", data: { slug: s, title: u.title } };
    }
    if (stateSet.has(s)) {
      const st = US_STATES_SEO.find((x) => x.slug === s)!;
      return { category: "state", data: { slug: s, title: st.title, abbr: st.abbr } };
    }
  }
  if (segments.length === 2) {
    const [ind, uc] = segments;
    if (industrySet.has(ind) && useCaseSet.has(uc)) {
      const i = INDUSTRIES.find((x) => x.slug === ind)!;
      const u = USE_CASES.find((x) => x.slug === uc)!;
      return {
        category: "cross",
        data: { slug: `${ind}/${uc}`, industry: i.title, useCase: u.title, industrySlug: ind, useCaseSlug: uc },
      };
    }
  }
  return { category: null, data: {} };
}

export function getAllSlugs(): string[][] {
  const slugs: string[][] = [];
  for (const t of NDA_TYPES) slugs.push([t.slug]);
  for (const i of INDUSTRIES) slugs.push([i.slug]);
  for (const u of USE_CASES) slugs.push([u.slug]);
  for (const s of US_STATES_SEO) slugs.push([s.slug]);
  for (const c of CROSS_COMBOS) slugs.push([c.industry, c.useCase]);
  return slugs;
}
